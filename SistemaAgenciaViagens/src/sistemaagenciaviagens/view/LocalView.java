    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        
        int codigo = Integer.parseInt(codigoField.getText());
        
        String cep = cepField.getText();
        String pais = paisField.getText();
        String estado = estadoField.getText();
        String cidade = cidadeField.getText();
        
        if(!((cep.equals("")) || (pais.equals("")) || (estado.equals("")) || (cidade.equals(""))))
        {
            try {
                this.controlador.salvar(codigo, cep, pais, estado, cidade);
                JOptionPane.showMessageDialog(rootPane, "Local Salvo com sucesso!");
                dadosPanel.setVisible(false);
                limparDados();
                atualizarTabela();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Campos com '*' são obrigatórios!");
        }
        
    }//GEN-LAST:event_salvarButtonActionPerformed

