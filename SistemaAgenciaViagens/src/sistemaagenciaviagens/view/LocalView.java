    private void limparDados(){
        cepField.setText("");
        paisField.setText("");
        estadoField.setText("");
        cidadeField.setText("");
    }
    
    private void atualizarTabela(){
        ((DefaultTableModel) dadosTable.getModel()).setRowCount(0);
        
        this.controlador.buscarTodos();
        
        for(int i=0; i<this.controlador.getListaDeLocais().size(); i++){
            
            ((DefaultTableModel) dadosTable.getModel()).addRow(new Object[]{
                this.controlador.getListaDeLocais().get(i).getCodigo(),
                this.controlador.getListaDeLocais().get(i).getCepDestino(),
                this.controlador.getListaDeLocais().get(i).getPaisDestino(),
                this.controlador.getListaDeLocais().get(i).getEstadoDestino(),
                this.controlador.getListaDeLocais().get(i).getCidadeDestino()
            });
            
        }
    }
    
